public class AnaliseAgua {

    private String nomeAnalise;
    private String unidadeMedida;
    private String vmp;
    private String numeroAmostraPrevista;
    private String numeroAmostraRealizada;
    private String valorMedio;
    private String valorMinimo;
    private String valorMaximo;
    private String foraPadrao;
    private String amostrasAposRecoleta;
    private String tipo;

    public String getNomeAnalise() {
        return nomeAnalise;
    }

    public void setNomeAnalise(String nomeAnalise) {
        this.nomeAnalise = nomeAnalise;
    }

    public String getUnidadeMedida() {
        return unidadeMedida;
    }

    public void setUnidadeMedida(String unidadeMedida) {
        this.unidadeMedida = unidadeMedida;
    }

    public String getVmp() {
        return vmp;
    }

    public void setVmp(String vmp) {
        this.vmp = vmp;
    }

    public String getNumeroAmostraPrevista() {
        return numeroAmostraPrevista;
    }

    public void setNumeroAmostraPrevista(String numeroAmostraPrevista) {
        this.numeroAmostraPrevista = numeroAmostraPrevista;
    }

    public String getNumeroAmostraRealizada() {
        return numeroAmostraRealizada;
    }

    public void setNumeroAmostraRealizada(String numeroAmostraRealizada) {
        this.numeroAmostraRealizada = numeroAmostraRealizada;
    }

    public String getValorMedio() {
        return valorMedio;
    }

    public void setValorMedio(String valorMedio) {
        this.valorMedio = valorMedio;
    }

    public String getValorMinimo() {
        return valorMinimo;
    }

    public void setValorMinimo(String valorMinimo) {
        this.valorMinimo = valorMinimo;
    }

    public String getValorMaximo() {
        return valorMaximo;
    }

    public void setValorMaximo(String valorMaximo) {
        this.valorMaximo = valorMaximo;
    }

    public String getForaPadrao() {
        return foraPadrao;
    }

    public void setForaPadrao(String foraPadrao) {
        this.foraPadrao = foraPadrao;
    }

    public String getAmostrasAposRecoleta() {
        return amostrasAposRecoleta;
    }

    public void setAmostrasAposRecoleta(String amostrasAposRecoleta) {
        this.amostrasAposRecoleta = amostrasAposRecoleta;
    }

    public String getTipo() {
        return tipo;
    }

    public void setTipo(String tipo) {
        this.tipo = tipo;
    }
}
